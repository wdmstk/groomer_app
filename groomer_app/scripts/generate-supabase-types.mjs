import { execSync } from 'node:child_process'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'

const projectId = process.env.SUPABASE_PROJECT_ID
if (!projectId) {
  console.error('Missing SUPABASE_PROJECT_ID. Example: set SUPABASE_PROJECT_ID=abcd1234')
  process.exit(1)
}

const outputPath = resolve(process.cwd(), 'src/lib/supabase/database.types.ts')
const outputDir = dirname(outputPath)
mkdirSync(outputDir, { recursive: true })

const command = `npx supabase gen types typescript --project-id ${projectId} --schema public`

try {
  const generated = execSync(command, {
    stdio: ['ignore', 'pipe', 'pipe'],
    encoding: 'utf-8',
  })

  const banner = [
    '// This file is generated by scripts/generate-supabase-types.mjs',
    '// Do not edit manually.',
    '',
  ].join('\n')

  writeFileSync(outputPath, `${banner}${generated}`, 'utf-8')
  console.log(`Generated Supabase types: ${outputPath}`)
} catch (error) {
  const message =
    error && typeof error === 'object' && 'stderr' in error
      ? String(error.stderr)
      : String(error)
  console.error('Failed to generate Supabase types.')
  console.error(message)
  process.exit(1)
}

